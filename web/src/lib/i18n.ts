const CATALOGS: Record<AppLocale, MessageCatalog> = {
  'zh-CN': zhCN,
  'en-US': enUS,
};

export function isSupportedLocale(value: string | null | undefined): value is AppLocale {
  return SUPPORTED_LOCALES.includes(value as AppLocale);
}

export function normalizeLocale(value: string | null | undefined): AppLocale {
  if (!value) return DEFAULT_LOCALE;
  if (isSupportedLocale(value)) return value;

  const normalized = value.toLowerCase();
  if (normalized.startsWith('zh')) return 'zh-CN';
  if (normalized.startsWith('en')) return 'en-US';
  return DEFAULT_LOCALE;
}

function readCookieLocaleFromDocument(): AppLocale | null {
  if (typeof document === 'undefined') return null;

  const cookieValue = document.cookie
    .split('; ')
    .find((entry) => entry.startsWith(`${LOCALE_COOKIE_KEY}=`))
    ?.split('=')[1];

  if (!cookieValue) return null;
  return normalizeLocale(decodeURIComponent(cookieValue));
}

export function getStoredLocale(): AppLocale | null {
  if (typeof window === 'undefined') return DEFAULT_LOCALE;
  try {
    const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    if (stored) {
      return normalizeLocale(stored);
    }
  } catch {
    return readCookieLocaleFromDocument();
  }

  return readCookieLocaleFromDocument();
}

export function detectBrowserLocale(): AppLocale {
  if (typeof navigator === 'undefined') return DEFAULT_LOCALE;
  const candidates = Array.isArray(navigator.languages) ? navigator.languages : [navigator.language];
  return normalizeLocale(candidates.find(Boolean));
}

export function resolveClientLocale(): AppLocale {
  const stored = getStoredLocale();
  if (stored) return stored;
  return detectBrowserLocale();
}

export function persistLocale(locale: AppLocale) {
  if (typeof window === 'undefined') return;

  try {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  } catch {
    // Ignore storage failures.
  }

  if (typeof document !== 'undefined') {
    document.cookie = `${LOCALE_COOKIE_KEY}=${encodeURIComponent(locale)}; path=/; max-age=${LOCALE_COOKIE_MAX_AGE}; samesite=lax`;
  }
}

export function resolveServerLocale(cookieValue: string | null | undefined, acceptLanguage: string | null | undefined): AppLocale {
  if (cookieValue) {
    return normalizeLocale(cookieValue);
  }

  if (!acceptLanguage) {
    return DEFAULT_LOCALE;
  }

  const candidates = acceptLanguage
    .split(',')
    .map((part) => part.split(';')[0]?.trim())
    .filter(Boolean);

  return normalizeLocale(candidates[0]);
}

export function translateForLocale(
  locale: AppLocale,
  key: string,
  values?: TranslationValues,
): string {
  const template = CATALOGS[locale][key] ?? CATALOGS[DEFAULT_LOCALE][key] ?? key;
  if (!values) return template;

  return Object.entries(values).reduce((message, [name, value]) => {
    return message.replaceAll(`{${name}}`, String(value));
  }, template);
}

export function translateClientText(key: string, values?: TranslationValues, locale?: AppLocale): string {
  return translateForLocale(locale ?? resolveClientLocale(), key, values);
}

export function formatDateForLocale(
  locale: AppLocale,
  value: string | number | Date,
  options?: Intl.DateTimeFormatOptions,
): string {
  return new Intl.DateTimeFormat(locale, options).format(new Date(value));
}

export function formatNumberForLocale(locale: AppLocale, value: number, options?: Intl.NumberFormatOptions): string {
  return new Intl.NumberFormat(locale, options).format(value);
}

export function getLocaleLabel(locale: AppLocale): string {
  return locale === 'zh-CN' ? '中文' : 'English';
}
